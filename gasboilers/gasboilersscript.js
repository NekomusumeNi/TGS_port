var table1 = document.getElementById('table1');
var table1click = 0;
table1.onclick = function() {
	let table1showName = table1.id + 'show';
	let table1show = document.getElementById(table1showName);
	table1show.classList.toggle('dn');
	if (table1click == 0) {
	table1.innerHTML = 'Характеристики <img style="margin-left: 10px;" src="gasboilersimages/arrowtop.png">';
	table1click = 1;
	}else{
		table1.innerHTML = 'Характеристики <img style="margin-left: 10px;" src="gasboilersimages/arrowbottom.png">';
		table1click = 0;
	}
}
